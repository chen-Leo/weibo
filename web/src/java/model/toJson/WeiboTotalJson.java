package model.toJson;


public class WeiboTotalJson {
    private int weiboId;
    private String weiboPhoto;
    private String mainContent;
    private String weiboUserName;
    private String weiboTime;
    private String[] weiboContents;
    private int isLike;

    public int getWeiboId() {
        return weiboId;
    }

    public void setWeiboId(int weiboId) {
        this.weiboId = weiboId;
    }

    public String getWeiboPhoto() {
        return weiboPhoto;
    }

    public void setWeiboPhoto(String weiboPhoto) {
        this.weiboPhoto = weiboPhoto;
    }

    public String getMainContent() {
        return mainContent;
    }

    public void setMainContent(String mainContent) {
        this.mainContent = mainContent;
    }

    public String getWeiboUserName() {
        return weiboUserName;
    }

    public void setWeiboUserName(String weiboUserName) {
        this.weiboUserName = weiboUserName;
    }

    public String getWeiboTime() {
        return weiboTime;
    }

    public void setWeiboTime(String weiboTime) {
        this.weiboTime = weiboTime;
    }

    public String[] getWeiboContents() {
        return weiboContents;
    }

    public void setWeiboContents(String[] weiboContents) {
        this.weiboContents = weiboContents;
    }

    public int getIsLike() {
        return isLike;
    }

    public void setIsLike(int isLike) {
        this.isLike = isLike;
    }































}
